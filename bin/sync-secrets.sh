#!/usr/bin/env bash
# Pull secrets from Vaultwarden (vault.neurago.ai) into a local .env.local file.
# Reads secrets.manifest.json for the env-var → vault-item mapping.
#
# Required env (set in your shell, NEVER commit):
#   BW_CLIENTID      personal API key client id  (Settings → Security → API Key)
#   BW_CLIENTSECRET  personal API key client secret
#   BW_PASSWORD      master password (only needed for unlock)
#
# Optional:
#   MANIFEST=./secrets.manifest.json   (default)
#   OUTPUT=./.env.local                (default — overrides outputFile in manifest)
#   ENV_NAME=staging|beta|production   (default: empty — pulls base secrets)
#
# Usage:
#   bin/sync-secrets.sh                       # pull all secrets to .env.local
#   bin/sync-secrets.sh --dry-run             # show what would be pulled, no writes
#   ENV_NAME=staging bin/sync-secrets.sh      # pull staging-scoped secrets
#
# Output file is chmod 600. Values containing literal $ are escaped to $$ so
# Docker Compose env-file interpolation doesn't mangle them.
set -euo pipefail

DRY_RUN=false
[ "${1:-}" = "--dry-run" ] && DRY_RUN=true

MANIFEST="${MANIFEST:-./secrets.manifest.json}"
OUTPUT="${OUTPUT:-./.env.local}"
BW_BIN="${BW_BIN:-/opt/homebrew/bin/bw}"
[ -x "$BW_BIN" ] || BW_BIN="$(command -v bw 2>/dev/null || true)"
[ -n "$BW_BIN" ] || { echo "ERROR: bw CLI not installed. brew install bitwarden-cli" >&2; exit 1; }

[ -f "$MANIFEST" ] || { echo "ERROR: $MANIFEST not found" >&2; exit 1; }

# ── 1. Make sure we're logged in + unlocked ───────────────────────────────
STATUS=$("$BW_BIN" status | python3 -c "import json,sys; print(json.load(sys.stdin)['status'])")
case "$STATUS" in
  unauthenticated)
    [ -n "${BW_CLIENTID:-}" ] || { echo "ERROR: BW_CLIENTID not set"  >&2; exit 1; }
    [ -n "${BW_CLIENTSECRET:-}" ] || { echo "ERROR: BW_CLIENTSECRET not set" >&2; exit 1; }
    echo "→ bw login --apikey"
    BW_CLIENTID="$BW_CLIENTID" BW_CLIENTSECRET="$BW_CLIENTSECRET" "$BW_BIN" login --apikey
    STATUS="locked"
    ;;
esac
if [ "$STATUS" = "locked" ]; then
  [ -n "${BW_PASSWORD:-}" ] || { echo "ERROR: BW_PASSWORD not set (needed to unlock)" >&2; exit 1; }
  echo "→ bw unlock"
  export BW_SESSION="$("$BW_BIN" unlock "$BW_PASSWORD" --raw)"
  [ -n "$BW_SESSION" ] || { echo "ERROR: failed to unlock vault" >&2; exit 1; }
fi

# Make sure local cache is fresh
echo "→ bw sync"
"$BW_BIN" sync >/dev/null

# ── 2. For each manifest entry, pull item + extract field ─────────────────
TMP=$(mktemp)
trap "rm -f $TMP" EXIT
echo "# Generated by bin/sync-secrets.sh on $(date -u +%Y-%m-%dT%H:%M:%SZ)" > "$TMP"
echo "# Source: vault.neurago.ai  Manifest: $MANIFEST" >> "$TMP"
[ -n "${ENV_NAME:-}" ] && echo "# Environment: $ENV_NAME" >> "$TMP"
echo "" >> "$TMP"

count=0
missing=0
python3 <<'PYEOF' "$MANIFEST" "$BW_BIN" "${ENV_NAME:-}" "$DRY_RUN" >> "$TMP" 2>/tmp/sync-secrets.log
import json, subprocess, sys, os
manifest_path, bw, env_name, dry_run = sys.argv[1], sys.argv[2], sys.argv[3], sys.argv[4] == 'true'
manifest = json.load(open(manifest_path))
for s in manifest['secrets']:
    item_name = s['item']
    field     = s['field']
    env_var   = s['envVar']
    required  = s.get('required', False)

    # If env_name is set, prefer items named "<base>/<env>" over base
    candidates = []
    if env_name: candidates.append(f"{item_name}/{env_name}")
    candidates.append(item_name)

    value = None
    found_in = None
    for cand in candidates:
        try:
            res = subprocess.run([bw, 'get', 'item', cand], capture_output=True, text=True, timeout=15)
            if res.returncode != 0:
                continue
            item = json.loads(res.stdout)
            # Search login.password, login.username, custom fields, notes
            login = item.get('login') or {}
            fields = item.get('fields') or []
            # Try the named field first
            for f in fields:
                if (f.get('name') or '').strip().lower() == field.strip().lower():
                    value = f.get('value'); found_in = f"fields[{field}]"; break
            # Then try login.password / login.username
            if value is None:
                if field.lower() in ('password','pass','secret','api-key','api_key','apikey','token'):
                    value = login.get('password'); found_in = 'login.password' if value else None
                elif field.lower() in ('username','user','client-id','client_id'):
                    value = login.get('username'); found_in = 'login.username' if value else None
            # Then notes (case it's a single-blob secret)
            if value is None and item.get('notes'):
                value = item['notes'].strip(); found_in = 'notes'
            if value is not None:
                break
        except Exception as e:
            print(f"# WARN ({env_var}): {e}", file=sys.stderr)

    if value is None:
        if required:
            print(f"# MISSING-REQUIRED: {env_var} (item='{item_name}', field='{field}')")
            print(f"MISSING_REQUIRED:{env_var}", file=sys.stderr)
        else:
            print(f"# {env_var}=  # not found in vault (item='{item_name}', field='{field}')")
        continue

    # Escape $ for docker compose env-file interpolation safety
    safe = value.replace('$', '$$')
    print(f"{env_var}={safe}")
    print(f"PULLED:{env_var}:{found_in}", file=sys.stderr)
PYEOF

# ── 3. Write to output (atomic) ────────────────────────────────────────────
if [ "$DRY_RUN" = "true" ]; then
  echo ""
  echo "=== DRY RUN — would write to $OUTPUT ==="
  cat "$TMP"
else
  install -m 600 "$TMP" "$OUTPUT"
  echo ""
  echo "✓ Wrote $OUTPUT ($(wc -l <"$OUTPUT") lines, chmod 600)"
fi

# ── 4. Report what was found ──────────────────────────────────────────────
echo ""
echo "=== Summary ==="
pulled=$(grep -c '^PULLED:' /tmp/sync-secrets.log 2>/dev/null || echo 0)
missing=$(grep -c '^MISSING_REQUIRED:' /tmp/sync-secrets.log 2>/dev/null || echo 0)
echo "  pulled : $pulled"
echo "  missing required: $missing"
if [ "$missing" -gt 0 ]; then
  echo ""
  echo "  Missing items:"
  grep '^MISSING_REQUIRED:' /tmp/sync-secrets.log | sed 's/^MISSING_REQUIRED:/    - /'
  exit 2
fi
